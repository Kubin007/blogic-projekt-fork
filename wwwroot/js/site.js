function loadCategory(category) {
  const productsDiv = document.getElementById("products");
  productsDiv.innerHTML = "";

  if (!data[category]) {
    productsDiv.innerHTML = "<p>Žádné produkty k zobrazení.</p>";
    return;
  }

  data[category].forEach(product => {
    const col = document.createElement("div");
    col.className = "col-md-3 mb-4";
    col.innerHTML = `
      <div class="card h-100">
        <img src="${product.img}" class="card-img-top" alt="${product.title}">
        <div class="card-body">
          <h5 class="card-title">${product.title}</h5>
          <p class="card-text">${product.price}</p>
          <a href="#" class="btn btn-warning">Koupit</a>
        </div>
      </div>
    `;
    productsDiv.appendChild(col);
  });
}

// Spustí se po načtení stránky a zobrazí výchozí kategorii
window.onload = () => loadCategory('moje');
